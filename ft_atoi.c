#include "libft.h"
#include <stdio.h>

int	ft_atoi(const char *str)
{
	long			num;
	long			sign;
	int	i;

	num = 0;
	sign = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
		|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + str[i] - '0';
		i++;
	}
	return (num * sign);
}

int	main(void)
{

	char str[] = "2147483649123123213211";
	int var;
	int res;
	
	res = atoi(str);

	var = ft_atoi(str);
	printf("%d\n",var);
	printf("%d\n",res);
}
